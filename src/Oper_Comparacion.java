public class Oper_Comparacion {
    static void main(String[] args) {

        int valor = 90, valor2 = 90;


        var resultado = valor == valor2;
        System.out.println(resultado);
        System.out.println(valor == valor2);
        System.out.println(valor != valor2);
        System.out.println(valor > valor2);
        System.out.println(valor < valor2);
        System.out.println(valor <= valor2);
        System.out.println(valor >= valor2);
    }
}
